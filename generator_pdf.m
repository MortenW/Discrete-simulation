    'tNcs', 'pJobDone', 1,...
    'tCs', 'pJobDone', 1,...
    'tNcs', 'pTrigger', 1,...
    'tCs', 'pTrigger', 1,...
    'pTrigger', 'tRemove', 1,...
    'tRemove', 'pReady', 1,... 
    'pReady', 'tProcess', 1};